class Specialty {
    String name;
    int numberOfTreatments;

    public Specialty(String name, int numberOfTreatments) {
        this.name = name;
        this.numberOfTreatments = numberOfTreatments;
    }

    public String getName() {
        return name;
    }

    public int getNumberOfTreatments() {
        return numberOfTreatments;
    }
}


